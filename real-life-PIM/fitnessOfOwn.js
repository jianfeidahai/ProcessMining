function getFitness(arr,numOfTrace) {
    let placeStart=1,
        placeA=0,
        placeB=0,
        placeC=0,
        placeD=0,
        placeEnd=0;


    let m=0,
        c=0,
        r=0,
        p=1;


    function judgeIsEnable(num) {
        switch (num){
            case 'A':
                return placeStart?true:false;
                break;
            case 'B':
                return placeA?true:false;
                break;
            case 'C':
                return placeB?true:false;
                break;
            case 'D':
                return placeB?true:false;
                break;
            case 'F':
                return placeC?true:false;
                break;
            case 'E':
                return placeC?true:false;
                break;
            case 'J':
                return placeD?true:false;
                break;
            case 'G':
                return placeD?true:false;
                break;
            case 'I':
                return placeD?true:false;
                break;
            case 'H':
                return placeD?true:false;
                break;
        }
    }

    function enable(num) {
        switch (num){
            case 'A':
                placeStart--;
                placeA++;
                c++;
                p++;
                break;
            case 'B':
                placeA--;
                placeB++;
                c++;
                p++;
                break;
            case 'C':
                placeB--;
                placeC++;
                c++;
                p++;
                break;
            case 'D':
                placeB--;
                placeA++;
                c++;
                p++;
                break;
            case 'E':
                placeC--;
                placeD++;
                c++;
                p++;
                break;
            case 'F':
                placeC--;
                placeB++;
                c++;
                p++;
                break;
            case 'G':
                placeD--;
                placeC++;
                c++;
                p++;
                break;
            case 'H':
                placeD--;
                placeC++;
                c++;
                p++;
                break;
            case 'I':
                placeD--;
                placeC++;
                c++;
                p++;
                break;
            case 'J':
                placeD--;
                placeEnd++;
                c++;
                p++;
                break;
        }
    }

    function add(num){
        switch (num) {
            case 'A':
                placeStart++;
                m++;
                break;
            case 'B':
                placeA++;
                m++;
                break;
            case 'C':
                placeB++;
                m++;
                break;
            case 'D':
                placeB++;
                m++;
                break;
            case 'E':
                placeC++;
                m++;
                break;
            case 'F':
                placeC++;
                m++;
                break;
            case 'J':
                placeD++;
                m++;
                break;
            case 'G':
                placeD++;
                m++;
                break;
            case 'I':
                placeD++;
                m++;
                break;
            case 'H':
                placeD++;
                m++;
                break;
        }
    }

    for (let i=0;i<arr.length;i++){
        if (judgeIsEnable(arr[i])){
            enable(arr[i]);
        }else {
            add(arr[i]);
            enable(arr[i]);
        }
    }
    if (placeEnd){
        placeEnd--;
        c++;
    }
    r=placeStart+placeA+placeB+placeC+placeD+placeEnd;
//        console.log('m:'+m);
//        console.log('c:'+c);
//        console.log('r:'+r);
//        console.log('p:'+p);
//        console.log(numOfTrace*m,numOfTrace*c,numOfTrace*r,numOfTrace*p);
    item0.push(numOfTrace*m);
    item1.push(numOfTrace*c);
    item2.push(numOfTrace*r);
    item3.push(numOfTrace*p);
}

